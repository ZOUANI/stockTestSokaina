    public void calculQte() {
        try {
            rs = cnx.createStatement().executeQuery("select * from produit");
        } catch (SQLException ex) {
            Logger.getLogger(vente.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            while (rs.next()) {
                qte = rs.getDouble("quantite");
                if (qte >= Double.parseDouble(jTextField2.getText())) {
                    qte = qte - Double.parseDouble(jTextField2.getText());
                    String req = "update produit set quantite=" + qte + "where id='" + jComboBox2.getSelectedItem() + "'";

                    cnx.createStatement().executeUpdate(req);
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(vente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
