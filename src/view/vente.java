    private void afficher() {
        h.setRowCount(0);
        String req = "select * from vente";
        try {
            rs = cnx.createStatement().executeQuery(req);
            while (rs.next()) {
                code = rs.getString("code");
                art = rs.getString("articleid");
                qtte = rs.getDouble("quantite");
                date = rs.getDate("date");
                emp = rs.getString("employe");
                Object[] vente = {code, art, qtte, date, emp};
                h.addRow(vente);
            }
        } catch (SQLException ex) {
            Logger.getLogger(vente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

