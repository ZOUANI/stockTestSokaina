    private void afficher() {
     t.setRowCount(0);
        try {
            rs=cnx.createStatement().executeQuery("select * from entreesortie");
            while(rs.next()){
                code=rs.getString("code");
                atr=rs.getString("articleid");
                qtt=rs.getDouble("quantite");
                pr=rs.getDouble("prix unitaire");
                date = rs.getDate("date");
                emp=rs.getString("employe");
                activ=rs.getString("activité");
                Object[] entreesortie={code ,atr ,qtt, pr, date ,emp ,activ};
                t.addRow(entreesortie);
            }
        } catch (SQLException ex) {
            Logger.getLogger(entreeSortie.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
