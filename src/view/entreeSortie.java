    public void calculqte() {
        try {
            rs = cnx.createStatement().executeQuery("select * from produit");
            if (jRadioButton1.isSelected()) {
                Double qte = 0.0;

                while (rs.next()) {
                    qte = rs.getDouble("quantite");
                    qte = qte + Double.parseDouble(jTextField1.getText());
                    String req = "update produit set quantite=" + qte + "where id='" + jComboBox1.getSelectedItem() + "'";
                    cnx.createStatement().executeUpdate(req);
                }
            } else if (jRadioButton2.isSelected()) {
                Double qte = 0.0;

                while (rs.next()) {
                    qte = rs.getDouble("quantite");
                    qte = qte - Double.parseDouble(jTextField1.getText());
                    String req = "update produit set quantite=" + qte + "where id='" + jComboBox1.getSelectedItem() + "'";
                        cnx.createStatement().executeUpdate(req);

                }

            }

        } catch (SQLException ex) {
            Logger.getLogger(entreeSortie.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

